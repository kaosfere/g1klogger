def c_to_f(x):
    return x * 1.8 + 32


def mps_to_kt(x):
    return x * 1.94384


def rad_to_deg(x):
    return x * 57.2958


def fps2_to_g(x):
    return x * 0.031


def ran_to_f(x):
    return x - 459.67


def psf_to_psi(x):
    return x / 144


def m_to_nm(x):
    return x / 1852
